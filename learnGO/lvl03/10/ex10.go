package main

import (
	"fmt"
)


func main() {
	// Anote (à mão) o resultado das expressões:
	// 	fmt.Println(true && true) 
	// 	fmt.Println(true && false) 
	// 	fmt.Println(true || true) 
	// 	fmt.Println(true || false) 
	// 	fmt.Println(!true)

	fmt.Println(true && true)      // true
	fmt.Println(true && false)    // false
	fmt.Println(true || true)    // true
	fmt.Println(true || false)  // true
	fmt.Println(!true) 		   // false
}